package com.fg.vo;

public class MemberVO {
	private int member_idx;
	private String member_id;
	private String member_pwd;
	private String member_phonenumber;
	private String member_joindate;
	
	public MemberVO() {}

	public int getMember_idx() {
		return member_idx;
	}

	public void setMember_idx(int member_idx) {
		this.member_idx = member_idx;
	}

	public String getMember_id() {
		return member_id;
	}

	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}

	public String getMember_pwd() {
		return member_pwd;
	}

	public void setMember_pwd(String member_pwd) {
		this.member_pwd = member_pwd;
	}

	public String getMember_phonenumber() {
		return member_phonenumber;
	}

	public void setMember_phonenumber(String member_phonenumber) {
		this.member_phonenumber = member_phonenumber;
	}

	public String getMember_joindate() {
		return member_joindate;
	}

	public void setMember_joindate(String member_joindate) {
		this.member_joindate = member_joindate;
	}
	
	
	
}
